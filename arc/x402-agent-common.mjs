export function loadEnv(filePath = path.resolve("arc/.env")) {
  if (!fs.existsSync(filePath)) return;
  for (const line of fs.readFileSync(filePath, "utf8").split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#") || !trimmed.includes("=")) continue;
    const [key, ...rest] = trimmed.split("=");
    if (!process.env[key]) process.env[key] = rest.join("=").trim().replace(/^["']|["']$/g, "");
  }
}

export function parseArgs(args = process.argv.slice(2)) {
  const parsed = { _: [] };
  for (let i = 0; i < args.length; i++) {
    const item = args[i];
    if (!item.startsWith("--")) {
      parsed._.push(item);
      continue;
    }
    const [rawKey, inlineValue] = item.slice(2).split("=", 2);
    const key = rawKey.replace(/-([a-z])/g, (_, char) => char.toUpperCase());
    if (inlineValue !== undefined) {
      parsed[key] = inlineValue;
      continue;
    }
    const next = args[i + 1];
    if (!next || next.startsWith("--")) {
      parsed[key] = true;
      continue;
    }
    parsed[key] = next;
    i += 1;
  }
  return parsed;
}

export function readWalletStore(filePath) {
  if (!filePath) throw new Error("--wallet-store is required");
  const payload = JSON.parse(fs.readFileSync(filePath, "utf8"));
  const wallets = payload.wallets || {};
  const byAgent = new Map();
  const byAddress = new Map();
  for (const [agentId, raw] of Object.entries(wallets)) {
    const address = String(raw.address || raw.wallet_address || "").trim();
    if (!address) continue;
    const wallet = {
      agentId,
      address,
      normalizedAddress: normalizeAddress(address),
      privateKey: String(raw.private_key || raw.privateKey || ""),
      provider: String(raw.provider || payload.provider || "local"),
      lineageId: lineageIdFor(agentId, raw),
      raw,
    };
    byAgent.set(agentId, wallet);
    byAddress.set(wallet.normalizedAddress, wallet);
  }
  return {
    path: filePath,
    provider: payload.provider || "",
    byAgent,
    byAddress,
    wallets: [...byAgent.values()],
  };
}

export function walletForAgent(store, agentId, label = "agent") {
  const wallet = store.byAgent.get(agentId);
  if (!wallet) throw new Error(`Unknown ${label} ${agentId} in ${store.path}`);
  return wallet;
}

export function serviceForName(serviceName) {
  const service = SERVICES[serviceName];
  if (!service) {
    throw new Error(`Unsupported service ${serviceName}. Expected one of: ${Object.keys(SERVICES).join(", ")}`);
  }
  return service;
}

export function serviceNameForFindingAccess(accessLevel) {
  if (accessLevel === "shared") return "finding_shared";
  if (accessLevel === "private") return "finding_private";
  throw new Error(`Unsupported finding access level ${accessLevel}; expected shared or private`);
}

export function x402Price(serviceName) {
  return `$${serviceForName(serviceName).price}`;
}

export function normalizePrivateKey(value) {
  let normalized = String(value || "").trim();
  if (!normalized) return "";
  if (normalized.includes("=")) normalized = normalized.split("=").pop().trim();
  normalized = normalized.replace(/^['"]|['"]$/g, "").trim();
  return normalized.startsWith("0x") ? normalized : `0x${normalized}`;
}

export function normalizeAddress(value) {
  return String(value || "").toLowerCase();
}

export function sameLineage(a, b) {
  return Boolean(a?.lineageId && b?.lineageId && a.lineageId === b.lineageId);
}

export function appendJsonl(filePath, event) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.appendFileSync(filePath, JSON.stringify(event) + "\n");
}

export function writeJson(filePath, payload) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, JSON.stringify(payload, null, 2) + "\n");
}

export function buildReceipt({
  roundId,
  payerId,
  payeeId,
  amount,
  paymentType,
  resourceId,
  description,
  payment,
  sellerWallet,
  buyerWallet,
  serviceName,
  metadata = {},
}) {
  const transaction = payment?.transaction || "";
  const isOnchainHash = /^0x[0-9a-fA-F]{64}$/.test(transaction);
  const receiptId = transaction ? `x402:${transaction}` : `x402:${Date.now()}:${payerId}:${payeeId}`;
  return {
    event_type: "payment_receipt",
    receipt_id: receiptId,
    round_id: roundId || "x402_agent_market",
    payer_id: payerId,
    payee_id: payeeId,
    amount: Number(amount),
    payment_type: paymentType,
    resource_id: resourceId || serviceName,
    description,
    metadata: {
      payment_rail: "x402_circle_gateway",
      chain: ARC_CHAIN_NAME,
      chain_id: ARC_CHAIN_ID,
      network: payment?.network || ARC_NETWORK,
      transaction,
      gateway_transfer_id: isOnchainHash ? "" : transaction,
      transaction_url: isOnchainHash ? `${ARC_EXPLORER}/tx/${transaction}` : "",
      payer_wallet: payment?.payer || buyerWallet?.address || "",
      payee_wallet: sellerWallet?.address || "",
      gateway_amount_atomic: payment?.amount || "",
      service_name: serviceName,
      ...metadata,
    },
  };
}

export function endpointForService(baseUrl, serviceName, sellerId) {
  const clean = baseUrl.replace(/\/+$/, "");
  if (serviceName === "summary") return `${clean}/ants/${encodeURIComponent(sellerId)}/summary`;
  if (serviceName === "audit") return `${clean}/ants/${encodeURIComponent(sellerId)}/audit`;
  if (serviceName === "finding_shared") return `${clean}/scouts/${encodeURIComponent(sellerId)}/findings/shared`;
  if (serviceName === "finding_private") return `${clean}/scouts/${encodeURIComponent(sellerId)}/findings/private`;
  throw new Error(`Unsupported service ${serviceName}`);
}

export function usageServices() {
  return Object.entries(SERVICES)
    .map(([name, service]) => `  ${name.padEnd(15)} ${service.price} USDC  ${service.description}`)
    .join("\n");
}

function envPrice(key, fallback) {
  return String(process.env[key] || fallback);
}

function lineageIdFor(agentId, wallet) {
  return String(
    wallet.lineage_id
      || wallet.lineageId
      || wallet.verified_lineage_id
      || wallet.world_lineage_id
      || wallet.identity?.lineage_id
      || wallet.identity?.lineageId
      || wallet.dynamic?.lineage_id
      || "",
  ).trim();
}
